# WHILE LOOPS

# A While loop will execute a set of statements as long as a condition is false
# Think of video games, as long as our video game is {on} / playing the code will
# run allowing the video game to be played

#but once the character runs out of lives, or beats the game, the game ends and the loop is finished

# Example 1 - Easy while loop
i = 1
while i < 6:
    print(i)  # print the value of i
    i+=1      # add one to i

# This code will run continuously until our i variable is at the number 6
# Notice the formatting is similar to our if/else statements with the colon : and our indentation

# We can actually even use ELSE Staments with our while loops

# ELSE STATEMENT WITH WHILE LOOP EXAMPLE
i2 = 1
while i2 < 6:
    print(i2)
    i2+=1
else:
    print("i2 is no longer less than 6")

# This code will print a condition once i2 is no longer less than 6



